

// traditional function definition

function add_traditional(a, b) {
  return a + b;
}

// anonymous function expression

const add_expression = function(a,b) {
  return a + b;
}

// arrow function expression with explicit return

const add_arrow = (a, b) => {
  return a + b;
};

// arrow function expression with implicit return
// one liner arrow function with implicit return

const add_arrow_implicit = (a,b) => a + b;

// one liner arrow function with implicit return and single argument

const double = a => a * 2;



